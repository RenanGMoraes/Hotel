    private void carregaTabela() {
        tabelaReservas.setModel(modelo);

        modelo.addColumn("Reserva");
        modelo.addColumn("CPF Alocado");
        modelo.addColumn("Quarto");
        modelo.addColumn("Data entrada");
        modelo.addColumn("Data saida");

        tabelaReservas.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabelaReservas.getColumnModel().getColumn(1).setMinWidth(100);
        tabelaReservas.getColumnModel().getColumn(2).setPreferredWidth(20);
        tabelaReservas.getColumnModel().getColumn(3).setPreferredWidth(50);
        tabelaReservas.getColumnModel().getColumn(4).setPreferredWidth(50);

        pesquisar(modelo);

    }

    public void pesquisar(DefaultTableModel model) {

        try {
            model.setRowCount(0);//Limpa linhas da tabela
            ReservaDAO dao = new ReservaDAO();

            for (Reserva r : dao.exibirViewTabelaReserva()) {
                model.addRow(new Object[]{r.getId(), r.getHospede().getCpf(),
                    r.getQuarto().getId(), r.getDataEntrada(), r.getDataSaida()});

            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar a tabela de reservas\n"
                    + "Erro de classe não encontrada:\n" + ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar a tabela de reservas\n"
                    + "Erro SQL:\n" + ex.getMessage());
        }
    }

