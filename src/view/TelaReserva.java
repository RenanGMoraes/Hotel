    private void btnCancelarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarReservaActionPerformed

        if (tabelaReservas.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione uma reserva da tabela");
            return;
        }

        int valor = (int) tabelaReservas.getValueAt(tabelaReservas.getSelectedRow(), 0);

        int op = JOptionPane.showConfirmDialog(this, "Deseja realmente cancelar a reserva " + valor + "?",
                "Confirmação", JOptionPane.YES_NO_OPTION);

        if (op == JOptionPane.YES_OPTION) {

            try {

                ReservaDAO reservaDao = new ReservaDAO();
                Reserva reserva = reservaDao.buscarPorId(valor);
                reservaDao.excluir(reserva);

                QuartoDAO quartoDao = new QuartoDAO();
                Quarto quarto = quartoDao.buscarPorId(reserva.getQuarto().getId());
                quartoDao.desocuparQuarto(quarto);

                JOptionPane.showMessageDialog(this, "Reserva cancelada. Quarto descocupado!");

            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Erro ao cancelar reserva\n"
                        + "Erro de classe não encontrada:\n" + ex.getMessage());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro ao cancelar reserva\n"
                        + "Erro SQL:\n" + ex.getMessage());
            }
        }

        pesquisar(modelo);
    }//GEN-LAST:event_btnCancelarReservaActionPerformed

