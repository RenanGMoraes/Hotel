    private void btnCadastrarEstadiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarEstadiaActionPerformed
        if (tabelaEstadia.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione uma reserva da tabela!");
            return;
        }

        try {

            EstadiaDAO estadiaDao = new EstadiaDAO();
            ReservaDAO reservaDao = new ReservaDAO();

            Reserva reserva = reservaDao.buscarPorId(Integer.parseInt(lblIdReserva.getText()));

            Estadia estadia = new Estadia();

            estadia.setDataEntrada(criaData(txtDataEntrada.getText()));
            estadia.setValorTotalServicos(0);
            estadia.setReserva(reserva);

            estadiaDao.cadastrar(estadia);
            reservaDao.alocaEstadiaReserva(reserva);

            pesquisar(modelo);

            JOptionPane.showMessageDialog(this, "Nova estadia cadastrada!");
            
            txtDataEntrada.setText("");
            lblIdReserva.setText("0");

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadastroEstadia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroEstadia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao formatar a data para cadastro:\n" + ex.getMessage());
        }

    }//GEN-LAST:event_btnCadastrarEstadiaActionPerformed

